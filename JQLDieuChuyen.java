    private void jButtonThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonThemActionPerformed
        // TODO add your handling code here:
        //chonts = jTableChonTS.getSelectedRow();        
        if(chonts==-1)
            JOptionPane.showMessageDialog(null, "Hãy chọn tài sản để điều chuyển!\n","Thông báo", JOptionPane.WARNING_MESSAGE);
        else if(jTextDCToi.getText().trim().length() == 0 ||
                jCBQL.getText().trim().length() == 0)
            JOptionPane.showMessageDialog(null, "Nơi điều chuyển và cán bộ quản lý không được để trống!\n","Thông báo", JOptionPane.WARNING_MESSAGE);                    
        else{
            try {
                tsan = dsTSWork.get(chonts);
                dchuyen = new TSDieuChuyen(jTextDCToi.getText(),jCBQL.getText(),jCBSD.getText(),
                        jViTri.getText(),format.parse(jNgaySD.getText()),tsan);
                if(dsDC.contains(dchuyen))
                    JOptionPane.showMessageDialog(null, "Tài sản này đang được điều chuyển, hãy chọn tài sản khác!\n","Lỗi", JOptionPane.ERROR_MESSAGE);
                else{
                    dsDC.add(dchuyen);
                    LoadTableDC(dsDC);
                    saveData();
                    resetTxt();
                }
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Hãy kiểm tra dữ liệu, định dạng ngày kiểu MM/dd/yyyy!\n" + ex,"Lỗi", JOptionPane.ERROR_MESSAGE);
//                Logger.getLogger(JQLDieuChuyen.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButtonThemActionPerformed

