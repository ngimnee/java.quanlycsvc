    private void jcboDMChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcboDMChonActionPerformed
        // TODO add your handling code here:
        ArrayList<TaiSan> dsLoc = new ArrayList<>();
        String itemLoc = jcboDMChon.getSelectedItem().toString();
        if(itemLoc.equalsIgnoreCase("Tất cả")){
            LoadTableTSC(dsTaiSan);
            dsTSWork=dsTaiSan;
        }            
        else{
            for(TaiSan itm : dsTaiSan) 
                if(itm.getDanhMuc().equalsIgnoreCase(itemLoc)) dsLoc.add(itm);
            LoadTableTSC(dsLoc);
            dsTSWork=dsLoc;
        }
    }//GEN-LAST:event_jcboDMChonActionPerformed

